# Напишите программу в которой пользователь вводит три 
# целых числа, а программа проверяет является ли эти
# числа элементами арифметической последовательности. 
# В арифметической последовательности каждый новый член
# получается прибавлением к предыдущему определенного 
# фиксированного числа.

 

#a = int(input('Input a= '))
